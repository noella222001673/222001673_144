public class switchcase {
     
        public static void main(String[] args) {  
            //Declaring a variable for switch expression  
            int age=25;  
            //Switch expression  
            switch(age){  
            //Case statements  
            case 10: System.out.println("5");  
            break;  
            case 20: System.out.println("15");  
            break;  
            case 30: System.out.println("25");  
            break;  
            //Default case statement  
            default:System.out.println("Not in 5, 15 or 25");  
            }  
        }  
        }  

